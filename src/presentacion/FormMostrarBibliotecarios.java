    public void llenarTabla(List<Bibliotecario> lista) {
        modelo = new DefaultTableModel();
        Bibliotecario bibliotecario;
        String tipo;

        String columnas[] = {"Código", "Nombres", "Apellidos", "DNI", "Teléfono"};
        Object fila[] = new Object[columnas.length];
        modelo.setColumnIdentifiers(columnas);
        Iterator<Bibliotecario> iterador = lista.iterator();
        while (iterador.hasNext()) {
            bibliotecario = iterador.next();
            fila[0] = bibliotecario.getCodigo();
            fila[1] = bibliotecario.getNombres();
            fila[2] = bibliotecario.getApellidos();
            fila[3] = bibliotecario.getDni();
            fila[4] = bibliotecario.getTelefono();
            modelo.addRow(fila);
        }

        tblPrestatarios.setModel(modelo);
    }

