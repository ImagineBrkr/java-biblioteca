    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        lista = biblioteca.getListaBibliotecarios().getLista();
        boolean re = false;
        ArrayList<Persona> aux = new ArrayList<>();
        Iterator<Bibliotecario> iterador = lista.iterator();
        while (iterador.hasNext()) {
            aux.add(iterador.next());
        }
        List<Persona> aux2 = aux;

        if (rbCodigo.isSelected()) {
            Collections.sort(lista);
        } else if (rbNombre.isSelected()) {
            Collections.sort(aux2, new CompararPersonaPorNombre());
            re = true;
        } else if (rbApellidos.isSelected()) {
            Collections.sort(aux2, new CompararPersonaPorApellidos());
            re = true;
        } else if (rbDni.isSelected()) {
            Collections.sort(aux2, new CompararPersonaPorDNI());
            re = true;
        }

        if (re) {
            Iterator<Persona> iterador2 = aux2.iterator();
            lista.clear();
            while (iterador2.hasNext()) {
                lista.add(((Bibliotecario) iterador2.next()));
            }
        }

        llenarTabla(lista);
    }//GEN-LAST:event_btnMostrarActionPerformed

