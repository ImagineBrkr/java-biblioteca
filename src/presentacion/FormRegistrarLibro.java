    private void cbxTipoLItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxTipoLItemStateChanged
        int pos = cbxTipoL.getSelectedIndex();
        if (pos != -1) {
            activarCampos(true);
            switch (pos) {
                case 0:
                    txtUniversidadL.setEnabled(false);
                    AgregarGenerosLibro();
                    break;
                case 1:
                    cbxGeneroL.removeAllItems();
                    txtUniversidadL.setEnabled(false);
                    cbxGeneroL.setEnabled(false);
                    break;
                case 2:
                    txtEditorialL.setEnabled(false);
                    AgregarGenerosAcademico();
                    break;
            }
        } else {
            activarCampos(false);
        }
    }//GEN-LAST:event_cbxTipoLItemStateChanged

