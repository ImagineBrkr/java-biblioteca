    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String titulo = txtTituloL.getText().trim(), autor = txtAutorL.getText().trim(), editorial = txtEditorialL.getText().trim(), año = txtAñoL.getText().trim(), codigo = txtCodigoL.getText().trim(), universidad = txtUniversidadL.getText().trim();
        String genero;
        autor = autor.equals("") ? "Anónimo" : autor;
        editorial = editorial.equals("") ? "Desconocido" : editorial;
        universidad = universidad.equals("") ? "Desconocido" : universidad;
        año = año.equals("") ? "Desconocido" : año;

        try {
            if (cbxGeneroL.getSelectedIndex() > -1) {
            genero = cbxGeneroL.getItemAt(cbxGeneroL.getSelectedIndex());
            }
            else {
                genero = "Desconocido";
            }
        } catch (Exception e) {
            genero = "Desconocido";
        }
        int cantidad;

        try {
            cantidad = Integer.parseInt(txtCantidadL.getText().trim());
        } catch (Exception e) {
            cantidad = 0;
        }

        int tipo = cbxTipoL.getSelectedIndex();
        Documento documento = new Documento();

        switch (tipo) {
            case 0:
                documento = new Libro(autor, titulo, año, genero, editorial);
                break;
            case 1:
                documento = new Revista(autor, titulo, año, editorial);
                break;
            case 2:
                documento = new Academico(autor, titulo, año, universidad, genero);
                break;
        }

        try {
            if (!titulo.equals("") && !codigo.equals("")) {
                if (!biblioteca.getLibrosBiblioteca().CodigoEnUso(codigo)) {
                    if (cantidad > 0) {

                        LibroBiblioteca libro = new LibroBiblioteca();

                        biblioteca.AgregarLibroBiblioteca(new LibroBiblioteca(codigo, documento, cantidad));
                        activarCampos(false);
                        btnNuevo.setEnabled(true);

                    } else {
                        JOptionPane.showMessageDialog(null, "Ingrese una cantidad válida.", "Error", 0);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Código en uso.", "Error", 0);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese los valores.", "Error", 0);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

