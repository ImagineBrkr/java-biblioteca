    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        lista = biblioteca.getListaLibrosPrestados().getLista();

        if (rbCodigoBibliotecario.isSelected()) {
            Collections.sort(lista);
        } else if (rbCodigoPrestatario.isSelected()) {
            Collections.sort(lista, new CompararLibroPrestadoPorPrestatario());
        } else if (rbFecha.isSelected()) {
            Collections.sort(lista, new CompararLibroPrestadoPorFecha());
        } else if (rbCodigoLibro.isSelected()) {
            Collections.sort(lista, new CompararLibroPrestadoPorLibro());
        }

        llenarTabla(lista);
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnVolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVolverActionPerformed
        if (pos == -1) {
            new FormPresDirector(biblioteca, this).setVisible(true);
        } else {
            new FormPresBibliotecario(biblioteca, this, pos).setVisible(true);
        }
        this.dispose();
    }//GEN-LAST:event_btnVolverActionPerformed

