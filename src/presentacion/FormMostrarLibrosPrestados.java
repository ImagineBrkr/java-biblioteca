    public void llenarTabla(List<LibroPrestado> lista) {
        modelo = new DefaultTableModel();
        LibroPrestado libro;
        String tipo;

        String columnas[] = {"Código bibliotecario", "Código Prestatario", "Código Libro", "Fecha", "Plazo hasta", "Dentro del plazo"};
        Object fila[] = new Object[columnas.length];
        modelo.setColumnIdentifiers(columnas);
        Iterator<LibroPrestado> iterador = lista.iterator();
        while (iterador.hasNext()) {

            libro = iterador.next();
            fila[0] = libro.getCodigoEmpleado();
            fila[1] = libro.getCodigoCarnet();
            fila[2] = libro.getCodigoLibro();
            fila[3] = libro.getFechaPrestamo().toString();
            fila[4] = libro.getFechaPlazo().toString();
            fila[5] = libro.getDentroDelPlazo() ? "Sí" : "No";
            modelo.addRow(fila);

        }

        tblLibrosPrestados.setModel(modelo);
    }

